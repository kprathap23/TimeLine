package com.timeline.app.model.stream;

public class Hashtags {
	private int len;
	private String name;
	private int pos;

	public int getLen() {
		return this.len;
	}

	public void setLen(int len) {
		this.len = len;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPos() {
		return this.pos;
	}

	public void setPos(int pos) {
		this.pos = pos;
	}
}
